/// Extract structured checkpoint from conversation history using an LLM call.
/// Writes the result to `checkpoint_path`. If `notes_content` is provided,
/// it is included in the extraction prompt and should be cleared afterward.
pub async fn extract_checkpoint(
    messages: &[Message],
    provider: &dyn LlmProvider,
    system_prompt: &str,
    checkpoint_path: &Path,
    notes_content: Option<&str>,
) -> Result<(), RustyError> {
    let old_text = messages_to_text(messages);

    // Read existing checkpoint if it exists, so we can do incremental updates
    let existing = read_file_content(checkpoint_path).await;

    let mut prompt = String::from(CHECKPOINT_PROMPT);

    if let Some(ref existing_cp) = existing {
        if !existing_cp.trim().is_empty() {
            prompt.push_str("\n\nExisting checkpoint (update incrementally, preserve unchanged sections):\n");
            prompt.push_str(existing_cp);
        }
    }

    if let Some(notes) = notes_content {
        if !notes.trim().is_empty() {
            prompt.push_str("\n\nScratchpad notes from the session (reconcile into appropriate sections, then clear):\n");
            prompt.push_str(notes);
        }
    }

    prompt.push_str("\n\nConversation:\n");
    prompt.push_str(&old_text);

    prompt.push_str("\n\nProduce the full checkpoint with all 11 sections. \
        Use the exact `## §N Title` headers. Update sections incrementally — \
        preserve content that's still accurate, update what changed.");

    let request = MessageRequest {
        model: provider.model().to_string(),
        system: Some(system_prompt.to_string()),
        messages: vec![Message::user(&prompt)],
        tools: vec![],
        max_tokens: 2048,
        temperature: None,
        thinking_budget: None,
    };

    let mut stream = provider.create_message_stream(request).await?;
    let mut checkpoint = String::new();

    while let Some(event) = stream.next().await {
        match event? {
            StreamEvent::TextDelta(text) => checkpoint.push_str(&text),
            StreamEvent::Done { .. } => break,
            StreamEvent::Error(msg) => return Err(RustyError::Api(msg)),
            _ => {}
        }
    }

    // Ensure parent directory exists
    if let Some(parent) = checkpoint_path.parent() {
        tokio::fs::create_dir_all(parent).await.ok();
    }

    tokio::fs::write(checkpoint_path, &checkpoint).await.map_err(|e| {
        RustyError::Other(format!("Failed to write checkpoint: {e}"))
    })?;

    info!("Extracted checkpoint ({} chars)", checkpoint.len());
    Ok(())
}

// ── Tier 3: Full compaction ─────────────────────────────────────────────────

/// Compute the token threshold for a given tier fraction.
fn tier_threshold(context_window: u32, fraction: f64) -> usize {
    (context_window as f64 * fraction) as usize
}

/// Check if compaction is needed and perform it.
/// Handles the three-tier checkpoint system:
/// - Tier 1 (25%): micro-compaction (cheap placeholder replacement)
/// - Tier 2 (50%): structured extraction to checkpoint.md
/// - Tier 3 (75%): full LLM-based compaction
///
/// Returns the highest tier that was executed.
pub async fn maybe_compact(
    messages: &mut Vec<Message>,
    provider: &dyn LlmProvider,
    system_prompt: &str,
    context_window: u32,
    plan_text: Option<&str>,
    last_tier: CheckpointTier,
    notes_path: Option<&Path>,
    checkpoint_path: Option<&Path>,
) -> Result<CheckpointTier, RustyError> {
    let estimated_tokens = estimate_tokens(messages);
    let t1_threshold = tier_threshold(context_window, TIER1_FRACTION);
    let t2_threshold = tier_threshold(context_window, TIER2_FRACTION);
    let t3_threshold = tier_threshold(context_window, TIER3_FRACTION);

    let needs_t3 = messages.len() >= COMPACT_MESSAGE_THRESHOLD
        || estimated_tokens >= t3_threshold;

    // ── Tier 3: Full compaction ───────────────────────────────────────
    if needs_t3 && last_tier.as_u8() < CheckpointTier::Compacted.as_u8() {
        info!(
            "Tier 3 compaction: {} messages (~{} tokens, threshold {})",
            messages.len(), estimated_tokens, t3_threshold
        );

        // Read checkpoint.md if available to enrich the summary
        let checkpoint_context = if let Some(cp_path) = checkpoint_path {
            read_file_content(cp_path).await
        } else {
            None
        };

        // Also read any remaining notes
        let notes_context = if let Some(n_path) = notes_path {
            let content = read_file_content(n_path).await;
            // Clear notes after reading
            if content.is_some() {
                clear_file(n_path).await;
            }
            content
        } else {
            None
        };

        let split_point = messages.len().saturating_sub(KEEP_RECENT);
        let old_messages = &messages[..split_point];
        let recent_messages = &messages[split_point..];

        let old_text = messages_to_text(old_messages);

        let mut summary_prompt = String::new();
        if let Some(cp) = &checkpoint_context {
            if !cp.trim().is_empty() {
                summary_prompt.push_str("Previous checkpoint state:\n");
                summary_prompt.push_str(cp);
                summary_prompt.push_str("\n\n");
            }
        }
        if let Some(notes) = &notes_context {
            if !notes.trim().is_empty() {
                summary_prompt.push_str("Session notes:\n");
                summary_prompt.push_str(notes);
                summary_prompt.push_str("\n\n");
            }
        }
        summary_prompt.push_str(
            "Summarize the following conversation concisely. Your summary MUST include:\n\n\
             1. **Active intent**: The user's most ORIGINAL request that started this work, \
             VERBATIM BLOCK-QUOTED. This is the single most important thing to preserve. \
             If the user gave one request at the start, that is the intent — do not let it \
             be overwritten by intermediate exploration or tangential work.\n\
             2. **Next concrete action**: The single next step to take when resuming. \
             Be specific: name files to edit, commands to run, or changes to make.\n\
             3. **What was completed**: Key accomplishments and code changes made so far.\n\
             4. **Current state**: What is in progress. List SPECIFIC file paths being edited.\n\
             5. **Key decisions**: Technical choices and their rationale.\n\
             6. **Errors and fixes**: Issues encountered and how resolved.\n\n\
             CRITICAL RULES:\n\
             - §1 must contain a verbatim block-quoted user request (> \"exact words\"). \
             Use the user's FIRST substantive request, not a later clarification.\n\
             - §2 must be an actionable directive the agent can execute immediately.\n\
             - §4 must list the exact file paths the agent was reading or writing.\n\
             - The agent wakes up with ONLY this summary — it must know exactly where to continue.\n\
             - Do NOT include git status, git diff, or unrelated repository state in the summary. \
             Only include context directly relevant to the user's active request.\n\n"
        );
        summary_prompt.push_str(&old_text);

        let request = MessageRequest {
            model: provider.model().to_string(),
            system: Some(system_prompt.to_string()),
            messages: vec![Message::user(&summary_prompt)],
            tools: vec![],
            max_tokens: 2048,
            temperature: None,
            thinking_budget: None,
        };

        let mut stream = provider.create_message_stream(request).await?;
        let mut summary = String::new();

        while let Some(event) = stream.next().await {
            match event? {
                StreamEvent::TextDelta(text) => summary.push_str(&text),
                StreamEvent::Done { .. } => break,
                StreamEvent::Error(msg) => return Err(RustyError::Api(msg)),
                _ => {}
            }
        }

        debug!("Compacted {} messages into summary", split_point);

        let summary_with_plan = if let Some(plan) = plan_text {
            format!("{summary}\n\n{plan}")
        } else {
            summary
        };
        let mut new_messages = Vec::new();
        new_messages.push(Message::user(format!(
            "[Previous conversation summary]\n{summary_with_plan}"
        )));
        new_messages.push(Message::assistant(
            "Understood. I have the context from our previous conversation.",
        ));
        new_messages.extend_from_slice(recent_messages);

        *messages = new_messages;

        let new_tokens = estimate_tokens(messages);
        info!(
            "After compaction: {} messages (~{} tokens)",
            messages.len(),
            new_tokens
        );

        return Ok(CheckpointTier::Compacted);
    }

    // ── Tier 2: Structured extraction ─────────────────────────────────
    if estimated_tokens >= t2_threshold && last_tier.as_u8() < CheckpointTier::Extracted.as_u8() {
        info!(
            "Tier 2 extraction: ~{} tokens (threshold {})",
            estimated_tokens, t2_threshold
        );

        // Read and clear notes
        let notes_content = if let Some(n_path) = notes_path {
            let content = read_file_content(n_path).await;
            if content.is_some() {
                clear_file(n_path).await;
            }
            content
        } else {
            None
        };

        if let Some(cp_path) = checkpoint_path {
            extract_checkpoint(
                messages,
                provider,
                system_prompt,
                cp_path,
                notes_content.as_deref(),
            )
            .await?;
        }

        // Also run micro-compaction at this tier
        micro_compact(messages);

        return Ok(CheckpointTier::Extracted);
    }

    // ── Tier 1: Micro-compaction ──────────────────────────────────────
    if estimated_tokens >= t1_threshold && last_tier.as_u8() < CheckpointTier::Micro.as_u8() {
        info!(
            "Tier 1 micro-compaction: ~{} tokens (threshold {})",
            estimated_tokens, t1_threshold
        );
        if micro_compact(messages) {
            return Ok(CheckpointTier::Micro);
        }
    }

    Ok(last_tier)
}

// ── Force compaction (used by /compact command) ─────────────────────────────

/// Force compaction regardless of thresholds — used for /compact command.
/// If `plan_text` is provided, it is appended to the summary.
pub async fn force_compact(
    messages: &mut Vec<Message>,
    provider: &dyn LlmProvider,
    system_prompt: &str,
    plan_text: Option<&str>,
    notes_path: Option<&Path>,
    checkpoint_path: Option<&Path>,
) -> Result<bool, RustyError> {
    if messages.len() < 4 {
        return Ok(false);
    }

    info!("Force compacting: {} messages", messages.len());

    // Read checkpoint and notes for context
    let checkpoint_context = if let Some(cp_path) = checkpoint_path {
        read_file_content(cp_path).await
    } else {
        None
    };
    let notes_context = if let Some(n_path) = notes_path {
        let content = read_file_content(n_path).await;
        if content.is_some() {
            clear_file(n_path).await;
        }
        content
    } else {
        None
    };

    let split_point = messages.len().saturating_sub(KEEP_RECENT);
    let old_messages = &messages[..split_point];
    let recent_messages = &messages[split_point..];

    let old_text = messages_to_text(old_messages);

    let mut summary_prompt = String::new();
    if let Some(cp) = &checkpoint_context {
        if !cp.trim().is_empty() {
            summary_prompt.push_str("Previous checkpoint state:\n");
            summary_prompt.push_str(cp);
            summary_prompt.push_str("\n\n");
        }
    }
    if let Some(notes) = &notes_context {
        if !notes.trim().is_empty() {
            summary_prompt.push_str("Session notes:\n");
            summary_prompt.push_str(notes);
            summary_prompt.push_str("\n\n");
        }
    }
    summary_prompt.push_str(
        "Summarize the following conversation concisely. Your summary MUST include:\n\n\
         1. **Active intent**: The user's most ORIGINAL request, VERBATIM BLOCK-QUOTED.\n\
         2. **Next concrete action**: The single next step, with specific file paths.\n\
         3. **What was completed**: Key accomplishments and code changes.\n\
         4. **Current state**: What is in progress, specific files being edited.\n\
         5. **Key decisions**: Technical choices and their rationale.\n\n\
         CRITICAL: §1 must contain a verbatim block-quoted user request. \
         The agent wakes up with ONLY this summary.\n\n"
    );
    summary_prompt.push_str(&old_text);

    let request = MessageRequest {
        model: provider.model().to_string(),
        system: Some(system_prompt.to_string()),
        messages: vec![Message::user(&summary_prompt)],
        tools: vec![],
        max_tokens: 2048,
        temperature: None,
        thinking_budget: None,
    };

    let mut stream = provider.create_message_stream(request).await?;
    let mut summary = String::new();

    while let Some(event) = stream.next().await {
        match event? {
            StreamEvent::TextDelta(text) => summary.push_str(&text),
            StreamEvent::Done { .. } => break,
            StreamEvent::Error(msg) => return Err(RustyError::Api(msg)),
            _ => {}
        }
    }

    debug!("Force compacted {} messages into summary", split_point);

    let summary_with_plan = if let Some(plan) = plan_text {
        format!("{summary}\n\n{plan}")
    } else {
        summary
    };
    let mut new_messages = Vec::new();
    new_messages.push(Message::user(format!(
        "[Previous conversation summary]\n{summary_with_plan}"
    )));
    new_messages.push(Message::assistant(
        "Understood. I have the context from our previous conversation.",
    ));
    new_messages.extend_from_slice(recent_messages);

    *messages = new_messages;
    Ok(true)
}

// ── Helpers ─────────────────────────────────────────────────────────────────

/// Read file content, returning None if the file doesn't exist or is empty.
pub async fn read_file_content(path: &Path) -> Option<String> {
    match tokio::fs::read_to_string(path).await {
        Ok(content) if !content.trim().is_empty() => Some(content),
        _ => None,
    }
}

/// Truncate a file to zero length (clear it).
pub async fn clear_file(path: &Path) {
    let _ = tokio::fs::write(path, "").await;
}

/// Estimate token count for messages.
/// Counts ALL content blocks (text, tool results, tool use, thinking) rather
/// than just text, so compaction triggers when context is actually full.
pub fn estimate_tokens(messages: &[Message]) -> usize {
    let mut total_chars: usize = 0;
    for msg in messages {
        total_chars += 16; // per-message overhead (~4 tokens)
        match &msg.content {
            rusty_core::MessageContent::Text(text) => {
                total_chars += text.len();
            }
            rusty_core::MessageContent::Blocks(blocks) => {
                for block in blocks {
                    match block {
                        rusty_core::ContentBlock::Text { text } => {
                            total_chars += text.len();
                        }
                        rusty_core::ContentBlock::Thinking { thinking } => {
                            total_chars += thinking.len();
                        }
                        rusty_core::ContentBlock::ToolUse { id, name, input } => {
                            total_chars += id.len() + name.len();
                            total_chars += input.to_string().len();
                            total_chars += 32;
                        }
                        rusty_core::ContentBlock::ToolResult { content, tool_use_id, .. } => {
                            total_chars += content.len() + tool_use_id.len() + 32;
                        }
                        rusty_core::ContentBlock::Image { .. } => {
                            total_chars += 256;
                        }
                    }
                }
            }
        }
    }
    ((total_chars as f64 * 1.2 / 4.0).ceil() as usize).max(1)
}

pub fn messages_to_text(messages: &[Message]) -> String {
    let mut parts = Vec::new();
    for msg in messages {
        let role = match msg.role {
            Role::User => "User",
            Role::Assistant => "Assistant",
        };
        let text = msg.get_all_text();
        if !text.is_empty() {
            parts.push(format!("{role}: {text}"));
        }
    }
    parts.join("\n\n")
}

/// Extract a section from a checkpoint markdown file by its `## §N Title` header.
/// Returns the section body (everything until the next `## §` header or end of file).
#[allow(dead_code)] // used by tests; available for future rebuild-context consumers
pub(crate) fn extract_section<'a>(checkpoint: &'a str, section_header: &str) -> Option<&'a str> {
    let start = checkpoint.find(section_header)?;
    let body_start = start + checkpoint[start..].find('\n')?;
    let rest = &checkpoint[body_start..];
    let end = rest[1..]
        .find("\n## §")
        .map(|i| i + 1)
        .unwrap_or(rest.len());
    Some(&rest[..end])
}

/// Extract the last N user messages as plain text, for verbatim preservation
/// in the rebuild context. Skips compaction boundary messages and empty texts.
pub fn recent_user_messages(messages: &[Message], count: usize) -> Vec<String> {
    messages
        .iter()
        .rev()
        .filter(|m| m.role == Role::User)
        .filter_map(|m| {
            let text = m.get_all_text();
            if text.contains("[Previous conversation summary]")
                || text.contains("Conversation history was automatically compacted")
            {
                return None;
            }
            if text.trim().is_empty() {
                return None;
            }
            Some(text)
        })
        .take(count)
        .collect::<Vec<_>>()
        .into_iter()
        .rev()
        .collect()
}

/// Generate a tail-aware reminder based on the last message role in the
/// preserved tail. Mirrors MiMo Code's tail-aware system reminders.
pub fn tail_aware_reminder(messages: &[Message]) -> &'static str {
    let last = match messages.last() {
        Some(m) => m,
        None => return "",
    };
    match last.role {
        Role::Assistant => {
            if last.has_tool_use() {
                "The previous assistant turn made tool calls. Process the tool results \
                 and continue your work loop."
            } else {
                "The previous assistant turn ended. Check your task list before stopping \
                 again — if work remains, continue."
            }
        }
        Role::User => "",
    }
}

/// Render the full rebuild context that is injected as a synthetic user message
/// after Tier 3 compaction. Modeled after MiMo Code's renderRebuildContext().
///
/// Assembles: checkpoint.md content, notes.md content, recent verbatim user
/// messages, seam framing, and a tail-aware reminder into a single message
/// that tells the agent exactly where to resume.
pub async fn render_rebuild_context(
    checkpoint_path: Option<&Path>,
    notes_path: Option<&Path>,
    messages: &[Message],
) -> String {
    let mut lines: Vec<String> = Vec::new();

    // Header: explicit framing that this is a continuation
    lines.push(
        "This session is being continued from a previous conversation that was \
         compacted to save context space. The checkpoint and notes below cover \
         the earlier portion of the conversation."
            .to_string(),
    );
    lines.push(String::new());

    // Section 1: Session checkpoint (full body)
    if let Some(cp_path) = checkpoint_path {
        if let Some(checkpoint) = read_file_content(cp_path).await {
            if !checkpoint.trim().is_empty() {
                lines.push("## Session checkpoint".to_string());
                lines.push(checkpoint.trim().to_string());
                lines.push(String::new());
            }
        }
    }

    // Section 2: Session notes (full body)
    if let Some(n_path) = notes_path {
        if let Some(notes) = read_file_content(n_path).await {
            if !notes.trim().is_empty() {
                lines.push("## Session notes".to_string());
                lines.push(notes.trim().to_string());
                lines.push(String::new());
            }
        }
    }

    // Section 3: Recent user messages (verbatim)
    let recent = recent_user_messages(messages, 5);
    if !recent.is_empty() {
        lines.push("## Recent user input (verbatim)".to_string());
        for msg in &recent {
            if msg.len() > 2000 {
                lines.push(format!("> {}...(truncated)", &msg[..2000]));
            } else {
                lines.push(format!("> {msg}"));
            }
        }
        lines.push(String::new());
    }

    // Section 4: Seam framing — explicit resume instructions
    lines.push(
        "Recent messages are preserved below — the assistant turn (and any tool results) \
         you will see is real history, not pseudo-content. Continue your task by responding \
         to the most recent state."
            .to_string(),
    );
    lines.push(String::new());
    lines.push(
        "Resume directly. Do not acknowledge this memory dump, do not recap, \
         do not preface with \"I will continue\" or similar. Pick up the last task \
         as if the break never happened."
            .to_string(),
    );

    // Section 5: Tail-aware reminder
    let reminder = tail_aware_reminder(messages);
    if !reminder.is_empty() {
        lines.push(String::new());
        lines.push(reminder.to_string());
    }

    lines.join("\n")
}


    fn estimate_single_message() {
        let msgs = vec![Message::user("hello")]; // 5 chars + 16 overhead = 21
        let tokens = estimate_tokens(&msgs);
        assert_eq!(tokens, 7); // ceil(21 * 1.2 / 4) = 7
    }

